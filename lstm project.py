import yfinance as yf
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import datetime

from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout
from tensorflow.keras.callbacks import EarlyStopping

"""Download MSFT Data"""

today = datetime.date.today()

data = yf.download(
    "MSFT",
    start="2018-01-01",
    end=(today + datetime.timedelta(days=1)).strftime("%Y-%m-%d"),
    auto_adjust=False
)

"""Create Target (1 = Up, 0 = Down)"""

data['Direction'] = (data['Close'].shift(-1) > data['Close']).astype(int)
data.dropna(inplace=True)

"""Feature Selection & Scaling"""

features = ['Open', 'High', 'Low', 'Close', 'Volume']
X_data = data[features].values

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X_data)

sequence_length = 30

X, y = [], []

for i in range(len(X_scaled) - sequence_length):
    X.append(X_scaled[i:i+sequence_length])
    y.append(data['Direction'].values[i+sequence_length])

X = np.array(X)
y = np.array(y)

"""Train-Test Split (Time-Series)"""

split = int(len(X) * 0.8)

X_train = X[:split]
X_test = X[split:]

y_train = y[:split]
y_test = y[split:]

"""Build LSTM Model"""

model = Sequential([
    LSTM(64, return_sequences=True, input_shape=(sequence_length, X.shape[2])),
    Dropout(0.3),
    LSTM(32),
    Dropout(0.3),
    Dense(16, activation='relu'),
    Dense(1, activation='sigmoid')
])

model.compile(
    optimizer='adam',
    loss='binary_crossentropy',
    metrics=['accuracy']
)

early_stop = EarlyStopping(
    monitor='val_loss',
    patience=5,
    restore_best_weights=True
)

"""Train Model"""

history = model.fit(
    X_train, y_train,
    epochs=30,
    batch_size=32,
    validation_split=0.1,
    callbacks=[early_stop],
    verbose=1
)

"""Predict Next Day Direction"""

last_sequence = X_scaled[-sequence_length:]
last_sequence = np.expand_dims(last_sequence, axis=0)

next_day_prob = model.predict(last_sequence)[0][0]
next_day_direction = int(next_day_prob > 0.5)

print("\nNext-Day Prediction for MSFT:")
print("Predicted Direction (1=Up, 0=Down):", next_day_direction)

